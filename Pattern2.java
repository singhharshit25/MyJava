public class Pattern2{
    public static void main(String[] args) {
        
        for(int i=0;i<4;i++){
            System.out.println("* * * *");
        }
        System.out.println("_____________________________\n");
        int i =0;
        while (i<4) {
            System.out.println("* * * *");
            i++;
        }
    }
}