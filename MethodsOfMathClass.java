public class MethodsOfMathClass {
    public static void main(String[] args) {
        int a=-4;
        int b=6;
        System.out.println("Min= " + (int)Math.min(a, b));
        System.out.println("Max= "+(int)Math.max(a, b));
        System.out.println("Sqrt= "+Math.sqrt(b));
        System.out.println("Power= "+(int)Math.pow(a, b));
        System.out.println("Absolute Value= "+(int) Math.abs(a));
    }
    
}
