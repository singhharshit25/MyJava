//wap to read a number and  display first N natural number.
import java.util.*;
public class Q30 {
    public static void main(String[] args) {
        System.out.println("Enter number: ");
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for(int i =1;i<=n;i++){
            System.out.print(i+" ");
        
        }
    }
    
}
